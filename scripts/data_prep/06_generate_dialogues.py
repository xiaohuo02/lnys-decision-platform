# ─────────────────────────────────────────────────────────────
# 生成对话数据
# ─────────────────────────────────────────────────────────────
def fill_template(text: str) -> str:
    product = rng.choice(SKU_NAMES)
    related = rng.choice(SKU_NAMES)
    order_id = f"ORD{rng.integers(10000000, 99999999)}"
    return (text
            .replace("{product}", product)
            .replace("{related_product}", related)
            .replace("{order_id}", order_id))

print("生成对话数据...")
dialogues = []
for intent, templates in INTENT_TEMPLATES.items():
    queries  = templates["queries"]
    replies  = templates["replies"]
    n_target = 300
    for i in range(n_target):
        q = fill_template(queries[i % len(queries)])
        r = fill_template(replies[i % len(replies)])
        dialogues.append({
            "intent":        intent,
            "user_query":    q,
            "standard_reply": r,
            "confidence":    round(rng.uniform(0.75, 0.99), 3),
        })

df_dial = pd.DataFrame(dialogues)
df_dial = df_dial.sample(frac=1, random_state=SEED).reset_index(drop=True)
df_dial.to_csv(OUT_DIAL, index=False, encoding="utf-8-sig")
print(f"  对话数据: {len(df_dial):,} 条  意图分布: {df_dial['intent'].value_counts().to_dict()}")

